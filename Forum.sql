   Id_UTILISATEUR INT,
   Nom VARCHAR(50) NOT NULL,
   Pr√©nom VARCHAR(50) NOT NULL,
   email VARCHAR(50) NOT NULL,
   MDP VARCHAR(100) NOT NULL,
   image VARCHAR(50),
   PRIMARY KEY(Id_UTILISATEUR)
